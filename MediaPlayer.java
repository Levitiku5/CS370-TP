import java.io.*;

public class MediaPlayer {

    public void play(String mediaUrl) {
        // Implementation to play media from the URL
        System.out.println("Playing media from: " + mediaUrl);
    }

    public void pause() {
        // Implementation to pause playing media
        System.out.println("Media paused");
    }

    public void stop() {
        // Implementation to stop playing media
        System.out.println("Media stopped");
    }

    // More methods to control volume, playlists, etc.
}
